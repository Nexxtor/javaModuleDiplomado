package com.naldana;

public class Main {

    public static void main(String[] args) {
        System.out.println("This is a text"); // String
        System.out.println('A'); // Character
        System.out.println(10); // Integer
        System.out.println(9.5f); // Float
        System.out.println(0.97); // Double
        System.out.println(true); // Boolean
    }
}
